# \n  -> New Line
print("Hello\nWorld")

# \t  -> Tab Space
print("Hello\tWorld")

# \\  -> Backslash
print("This is a backslash: \\")

# \'  -> Single Quote
print('It\'s Python')

# \"  -> Double Quote
print("He said \"Hello\"")

# \r  -> Carriage Return
print("Hello\rWorld")

# \b  -> Backspace
print("Helloo\b")

# \f  -> Form Feed
print("Hello\fWorld")

# \ooo -> Octal value
print("\101")   # ASCII value for A

# \xhh -> Hexadecimal value
print("\x41")   # Hex value for A

# print statement
print('hello',"divya",26,sep=",")